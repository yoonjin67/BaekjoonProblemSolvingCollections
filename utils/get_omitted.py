import os

def find_existing_problem_folders(problem_numbers, root_dir='.'):
    """
    주어진 문제 번호 목록에 대해, 루트 디렉토리 아래에서
    번호가 포함된 폴더를 찾습니다.

    Args:
        problem_numbers (list): 백준 문제 번호 목록 (정수 또는 문자열).
        root_dir (str): 탐색을 시작할 루트 디렉토리 (기본값: 현재 디렉토리).

    Returns:
        set: 번호가 포함된 폴더가 존재하는 문제 번호의 집합.
    """
    existing_problems = set()

    # 모든 디렉토리 및 파일을 순회
    for root, dirs, files in os.walk(root_dir):
        for dir_name in dirs:
            # 디렉토리 이름에 숫자가 포함된 폴더를 찾기
            for problem_number in problem_numbers:
                if str(problem_number) in dir_name:
                    existing_problems.add(str(problem_number))  # 문제 번호를 set에 추가

    return existing_problems

